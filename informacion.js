function loadIslandInfo() {
    const urlParams = new URLSearchParams(window.location.search);
    const islandName = urlParams.get('isla');
    const island = islandsData[islandName];
    
    if (island) {
        const main = document.getElementById('island-info');
        main.innerHTML = `
            <div class="text-containerislasinfo">
                <h2>${island.title}</h2>
                <div class="descripcion-container">
                    <div class="descripcion">
                        <strong>Descripción </strong><p>${island.description}</p>
                    </div>
                    <img src="${island.imagen_1}" alt="${island.title}"></img>
                </div>
                
                <div class="caracteristicas-container">
                    <div class="ubicacion-container">
                        <p><strong>Ubicación</strong></p>
                        <a href=${island.Ubicacion} target="_blank">
                            <img src="images/Icono-Ubicacion.png" alt="Icono de ubicación">
                        </a>
                    </div>
                    
                    <strong>Tamaño:</strong><p class="Tamaño">${island.Tamaño}</p>
                    <strong>Características </strong><p>${island.Características}</p>
                    <strong>Lo que la hace Especial </strong><p>${island.Especial}</p>
                </div>
            </div>
        `;

        // Carrusel de imágenes de fauna
        if (island.Fauna_1 || island.Fauna_2 || island.Fauna_3) {
            const faunaImages = [island.Fauna_1, island.Fauna_2, island.Fauna_3].filter(Boolean);
            const faunaContainer = document.createElement('div');
            faunaContainer.classList.add('Fauna');
            faunaContainer.innerHTML = `
                <div class="fauna-carousel">
                    <img id="fauna-img" src="${faunaImages[0]}" alt="Fauna" style="position: relative;">
                </div>
                <div class="fauna-text">
                    <strong>Fauna </strong><p>${island.Fauna}</p>
                </div>
            `;
            main.appendChild(faunaContainer);

            let currentImageIndex = 0;
            const faunaImgElement = document.getElementById('fauna-img');

            setInterval(() => {
                faunaImgElement.classList.add('hidden'); // Oculta la imagen actual
                setTimeout(() => {
                    currentImageIndex = (currentImageIndex + 1) % faunaImages.length;
                    faunaImgElement.src = faunaImages[currentImageIndex];
                    faunaImgElement.classList.remove('hidden'); // Muestra la nueva imagen
                }, 500); // Espera 500ms para cambiar la imagen
            }, 3000); // Cambia cada 3 segundos
        }

        // Carrusel de imágenes de flora
        if (island.Flora_1 || island.Flora_2 || island.Flora_3) {
            const floraImages = [island.Flora_1, island.Flora_2, island.Flora_3].filter(Boolean);
            const floraContainer = document.createElement('div');
            floraContainer.classList.add('Flora');
            floraContainer.innerHTML = `
                <div class="flora-text">
                    <strong>Flora </strong><p>${island.Flora}</p>
                </div>
                <div class="flora-carousel">
                    <img id="flora-img" src="${floraImages[0]}" alt="Flora" style="position: relative;">
                </div>
            `;
            main.appendChild(floraContainer);

            let currentFloraIndex = 0;
            const floraImgElement = document.getElementById('flora-img');

            setInterval(() => {
                floraImgElement.classList.add('hidden'); // Oculta la imagen actual
                setTimeout(() => {
                    currentFloraIndex = (currentFloraIndex + 1) % floraImages.length;
                    floraImgElement.src = floraImages[currentFloraIndex];
                    floraImgElement.classList.remove('hidden'); // Muestra la nueva imagen
                }, 500); // Espera 500ms para cambiar la imagen
            }, 3000); // Cambia cada 3 segundos
        }

        // Carrusel de imágenes de turismo
        if (island.Turismo_1 || island.Turismo_2 || island.Turismo_3) {
            const turismoImages = [island.Turismo_1, island.Turismo_2, island.Turismo_3].filter(Boolean);
            const turismoContainer = document.createElement('div');
            turismoContainer.classList.add('Turismo');
            turismoContainer.innerHTML = `
                <div class="turismo-carousel">
                    <img id="turismo-img" src="${turismoImages[0]}" alt="Turismo" style="position: relative;">
                </div>
                <div class="turismo-text">
                    <strong>Turismo </strong><p>${island.Lugares_turísticos}</p>
                </div>
            `;
            main.appendChild(turismoContainer);

            let currentTurismoIndex = 0;
            const turismoImgElement = document.getElementById('turismo-img');

            setInterval(() => {
                turismoImgElement.classList.add('hidden'); // Oculta la imagen actual
                setTimeout(() => {
                    currentTurismoIndex = (currentTurismoIndex + 1) % turismoImages.length;
                    turismoImgElement.src = turismoImages[currentTurismoIndex];
                    turismoImgElement.classList.remove('hidden'); // Muestra la nueva imagen
                }, 500); // Espera 500ms para cambiar la imagen
            }, 3000); // Cambia cada 3 segundos
        }
    }
}

window.onload = loadIslandInfo;